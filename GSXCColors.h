// Reset
#define RESET         "\033[0m"       // Reset text

// Style
#define REGULAR       "\033[0;31m"    // Regular
#define BOLD          "\033[1;31m"    // Bold
#define LOW_INTENSITY "\033[2;31m"    // Low Intensity
#define ITALIC	      "\033[3;31m"    // Italic
#define UNDERLINE     "\033[4;31m"    // Underline
#define BLINKING      "\033[5;31m"    // Blinking
#define REVERSE       "\033[6;31m"    // Reverse
#define BACKGROUND    "\033[7;31m"    // Background
#define INVISIBLE     "\033[8;31m"    // Invisible

// Colors
#define	BLACK         "\033[0;30m"    // Black
#define RED           "\033[0;31m"    // Red
#define GREEN         "\033[0;32m"    // Green
#define YELLOW        "\033[0;33m"    // Yellow
#define BLUE          "\033[0;34m"    // Blue
#define MAGENTA       "\033[0;35m"    // Magenta
#define CYAN          "\033[0;36m"    // Cyan
#define WHITE         "\033[0;37m"    // White
